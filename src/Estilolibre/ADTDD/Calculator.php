<?php
namespace Estilolibre\ADTDD;
/**
 * Description of Calculator
 *
 * @author isidromerayo
 */
class Calculator {
    /**
     * 
     * @param int $arg1
     * @param int $arg2
     * @return int
     */
    public function add($arg1, $arg2) {
        return $arg1 + $arg2;
    }

    /**
     * 
     * @param int $arg1
     * @param int $arg2
     * @return int
     */
    public function substract($arg1, $arg2) {
        return $arg1 - $arg2;
    }

}
